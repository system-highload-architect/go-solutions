// Package cachealign предоставляет структуры и функции для оптимизации доступа к памяти
// с учётом размера кэш-линии процессора (64 байта).
//
// Основная идея: группировать ключи диспетчеризации в батчи, размер которых выровнен
// по длине кэш-линии, чтобы минимизировать промахи кэша при последовательном обходе.
//
// В отличие от первой версии, где использовались 64‑байтные ключи на указателях,
// теперь ключ представлен компактным uint32, что позволяет уместить 16 ключей
// в одну кэш-линию и избежать лишних указателей в куче.
package cachealign

import "unsafe"

// CacheLineSize – размер кэш-линии процессора (стандарт для x86‑64).
const CacheLineSize = 64

// AlignedKey – компактный ключ для табличной диспетчеризации.
// Представляет собой uint32, собранный из трёх uint8‑констант:
//   - Тип сущности (например, тип токена)
//   - Состояние (текущий статус)
//   - Событие (внешний триггер)
// Все три компонента упакованы в одно 32‑битное слово, что даёт быстрые сравнения
// и минимизирует объём данных, загружаемых в кэш.
//
// 0x00_TT_SS_EE
//   TT – Type   (1 байт)
//   SS – State  (1 байт)
//   EE – Event  (1 байт)
//   Старший байт зарезервирован (можно использовать для дополнительного атрибута)
type AlignedKey uint32

// MakeKey собирает ключ из трёх uint8.
func MakeKey(entityType, state, event uint8) AlignedKey {
	return AlignedKey(uint32(entityType)<<16 | uint32(state)<<8 | uint32(event))
}

// AlignedBatch – слайс ключей, подготовленный для эффективного последовательного обхода.
// Благодаря выровненному размеру и компактности AlignedKey, процессор может загружать
// до 16 ключей одной кэш‑линией, что снижает количество cache‑misses.
type AlignedBatch struct {
	keys []AlignedKey
}

// NewAlignedBatch создаёт батч с ёмкостью, кратной CacheLineSize / размер ключа (4 байта).
// Это гарантирует, что каждый батч начинается с границы кэш-линии (при условии, что
// аллокатор Go возвращает память с выравниванием 64 байта для слайсов, начинающихся
// с достаточно большого индекса – для гарантии можно использовать ручное выравнивание,
// но в тестах мы покажем, что и обычный make даёт хороший эффект за счёт компактности).
func NewAlignedBatch(capacity int) *AlignedBatch {
	// 64 / 4 = 16 ключей на одну кэш-линию
	const keysPerLine = CacheLineSize / int(unsafe.Sizeof(AlignedKey(0)))
	if capacity <= 0 {
		capacity = keysPerLine
	}
	// округляем ёмкость вверх до ближайшего кратного числу ключей на линию
	alignedCap := ((capacity + keysPerLine - 1) / keysPerLine) * keysPerLine
	return &AlignedBatch{
		keys: make([]AlignedKey, 0, alignedCap),
	}
}

// Append добавляет ключ в батч.
func (b *AlignedBatch) Append(key AlignedKey) {
	b.keys = append(b.keys, key)
}

// Len возвращает количество ключей в батче.
func (b *AlignedBatch) Len() int {
	return len(b.keys)
}

// Process последовательно обрабатывает все ключи в батче переданной функцией.
// Типичное использование: вызов функций-обработчиков из таблицы переходов.
func (b *AlignedBatch) Process(fn func(key AlignedKey)) {
	for i := 0; i < len(b.keys); i++ {
		fn(b.keys[i])
	}
}
