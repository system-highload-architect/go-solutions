// Пример использования пакета statistics.
//
// Задача: веб‑сервис должен проанализировать задержки ответов за последнюю минуту,
// вычислить среднее, медиану, 95‑й процентиль и стандартное отклонение,
// чтобы понять, насколько стабильно работает сервис.
package main

import (
	"fmt"

	"github.com/system-highload-architect/go-solutions/math/statistics"
)

func main() {
	// Задержки ответов в миллисекундах (выборка за последнюю минуту).
	latencies := []float64{
		45, 47, 50, 52, 48, 300, 51, 49, 53, 46,
		47, 55, 49, 50, 52, 48, 51, 47, 54, 50,
	}

	// 1. Среднее значение (может быть искажено выбросом).
	mean := statistics.Mean(latencies)
	fmt.Printf("Средняя задержка: %.1f мс\n", mean)

	// 2. Медиана (устойчива к выбросам).
	median := statistics.Median(latencies)
	fmt.Printf("Медианная задержка: %.1f мс\n", median)

	// 3. 95‑й процентиль (характеризует «худшие» 5% запросов).
	p95 := statistics.Percentile(latencies, 0.95)
	fmt.Printf("95‑й процентиль: %.1f мс\n", p95)

	// 4. Стандартное отклонение (разброс).
	stddev := statistics.StdDev(latencies)
	fmt.Printf("Стандартное отклонение: %.1f мс\n", stddev)

	// 5. Минимальное и максимальное значения.
	min, max := statistics.MinMax(latencies)
	fmt.Printf("Минимум: %.0f мс, Максимум: %.0f мс\n", min, max)

	// 6. Асимметрия (положительная — хвост справа, есть выбросы).
	skew := statistics.Skewness(latencies)
	fmt.Printf("Асимметрия: %.2f\n", skew)

	// Вывод: медиана около 50 мс, но p95 уже 300 мс из‑за одного выброса.
	// Это повод проверить логи и найти причину всплеска.
}
